function courseProgress(c=course()){
  const total=c.lessons.length;
  const doneTests=c.lessons.filter(l=>state.progress.tests[`${c.id}:${l.id}`]).length;
  const doneLessons=c.lessons.filter(l=>state.progress.completed[`${c.id}:${l.id}`]).length;
  const percent=total ? Math.round((doneTests/total)*100) : 0;
  const score=c.lessons.reduce((acc,l)=>acc+(state.progress.scores[`${c.id}:${l.id}`]||0),0);
  return {total, doneTests, doneLessons, percent, score};
}
