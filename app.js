function setupWidget(type){
  const update=()=>{
    const r=$('w-result'), n=$('w-note'); if(!r||!n) return;
    if(type==='inflation-calc'){ const sum=+$('w-sum').value, infl=+$('w-infl').value/100, years=+$('w-years').value; const future=sum/Math.pow(1+infl,years); r.textContent=`Покупательная способность: ${money(future)}`; n.textContent=`Через ${years} лет ${money(sum)} будут ощущаться примерно как ${money(future)} сегодня.`; }
    else if(type==='goal-calc'){ const goal=+$('w-goal').value, month=+$('w-month').value, rate=+$('w-rate').value/100/12, years=+$('w-years').value; let total=0; for(let i=0;i<years*12;i++) total=(total+month)*(1+rate); r.textContent= total>=goal?'Цель достижима':'Нужно увеличить взнос или срок'; n.textContent=`Расчетный капитал: ${money(total)} при цели ${money(goal)}.`; }
    else if(type.includes('iis')){ const sum=+$('w-sum').value, tax=+$('w-tax').value/100, years=+$('w-years').value; const base=Math.min(sum,400000), deduction=base*tax; r.textContent=`Потенциальный вычет: ${money(deduction)}`; n.textContent=`База вычета ${money(base)}. Срок ${years} лет: льготы важно сохранять до минимального срока.`; }
    else if(type.includes('bond')){ const price=+$('w-price').value/100, coupon=+$('w-coupon').value/100, years=+$('w-years').value, face=+$('w-face').value; const ytm=((face*coupon + (face-face*price)/years)/((face+face*price)/2))*100; r.textContent=`Ориентир YTM: ${pct(ytm)}`; n.textContent=`Цена покупки ${money(face*price)}, годовой купон ${money(face*coupon)}.`; }
    else if(type.includes('dividend')){ const price=+$('w-price').value, div=+$('w-div').value, qty=+$('w-qty').value; const income=div*qty*.87; r.textContent=`После НДФЛ: ${money(income)}`; n.textContent=`Дивидендная доходность до налога: ${pct(div/price*100)}. Проверьте календарь дивидендов АКБФ.`; }
    else if(type.includes('fund')){ const sum=+$('w-sum').value, a=+$('w-fee-a').value/100, b=+$('w-fee-b').value/100, years=+$('w-years').value; const diff=sum*((Math.pow(1-a,years))-(Math.pow(1-b,years))); r.textContent=`Разница из-за комиссии: ${money(Math.abs(diff))}`; n.textContent=`При прочих равных меньшая комиссия помогает сохранить больше результата.`; }
    else if(type.includes('gold')){ const sum=+$('w-sum').value, spread=+$('w-spread').value/100, growth=+$('w-growth').value/100, years=+$('w-years').value; const result=sum*Math.pow(1+growth,years)*(1-spread); r.textContent=`Расчетный результат: ${money(result)}`; n.textContent=`Спред и комиссия могут заметно снизить результат даже при росте цены металла.`; }
    else if(type.includes('stock')||type.includes('order')||type.includes('price')||type.includes('strategy')||type.includes('growth')||type.includes('value')||type.includes('multiples')){ const price=+$('w-price').value, qty=+$('w-qty').value, ch=+$('w-change').value/100, comm=+$('w-comm').value/100; const cost=price*qty, result=cost*(1+ch)*(1-comm); r.textContent=`Стоимость позиции: ${money(result)}`; n.textContent=`Сумма сделки ${money(cost)}. Перед покупкой в АКБФ проверьте счет, количество, цену и комиссию.`; }
    else { const s=+$('w-stocks').value,b=+$('w-bonds').value,g=+$('w-gold').value; r.textContent=`Итого распределено: ${s+b+g}%`; n.textContent=(s+b+g===100?'Портфель собран на 100%.':'Скорректируйте доли до 100%.'); }
  };
  document.querySelectorAll('#widgetBody input[type="range"]').forEach(input=>bindRange(input.id, val=> input.id.includes('tax')||input.id.includes('rate')||input.id.includes('infl')||input.id.includes('coupon')||input.id.includes('price')&&type.includes('bond')||input.id.includes('fee')||input.id.includes('spread')||input.id.includes('growth')||input.id.includes('change')||input.id.includes('comm') ? (input.id.includes('price')&&type.includes('bond')?`${val}%`: `${val}%`) : (input.id.includes('years')?`${val} лет`: money(val)), update));
  update();
}

