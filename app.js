function renderTest(l){
  const k=key(), answered=state.progress.answered[k] || {};
  const score=state.progress.scores[k] || 0;
  els.testPanel.innerHTML = `<div class="section-head"><div><div class="eyebrow">Проверка знаний</div><h3>Тест по уроку</h3><p class="panel-intro">После ответа вы сразу увидите правильный вариант и пояснение.</p></div></div><div class="test-list">${l.tests.map((t,qi)=>`<div class="test-card" data-test="${qi}"><div class="test-q">${qi+1}. ${t.q}</div><div class="answers">${t.answers.map((a,ai)=>`<button class="answer-btn" data-q="${qi}" data-a="${ai}">${a}</button>`).join('')}</div><div class="answer-expl hidden" id="expl-${qi}"></div></div>`).join('')}</div><div class="test-score" id="testScore">Правильных ответов: ${score} из ${l.tests.length}</div>${renderLessonActions()}`;
  Object.entries(answered).forEach(([q,a])=>applyAnswer(+q,+a,l.tests));
  els.testPanel.querySelectorAll('.answer-btn').forEach(btn=>btn.addEventListener('click',()=>{
    const q=+btn.dataset.q, a=+btn.dataset.a;
    if((state.progress.answered[k]||{})[q]!==undefined) return;
    state.progress.answered[k]={...(state.progress.answered[k]||{}), [q]:a};
    applyAnswer(q,a,l.tests);
    const entries=Object.entries(state.progress.answered[k]);
    const newScore=entries.reduce((acc,[idx,chosen])=>acc+(l.tests[idx].correct===+chosen?1:0),0);
    state.progress.scores[k]=newScore;
    if(entries.length===l.tests.length){ state.progress.tests[k]=true; state.progress.completed[k]=true; }
    $('testScore').textContent=`Правильных ответов: ${newScore} из ${l.tests.length}`;
    safeSave(); renderSidebar(); updateHeader();
  }));
}
