function calcInflation(){const sum=rangeVal('infl-sum'), rate=rangeVal('infl-rate')/100, years=rangeVal('infl-years');const future=sum/Math.pow(1+rate,years);return `<strong>${fmt(future)}</strong>Покупательная способность ${fmt(sum)} через ${years} лет при такой инфляции.`}
function calcGoal(){const target=rangeVal('goal-target'),start=rangeVal('goal-start'),month=rangeVal('goal-month'),y=rangeVal('goal-yield')/100/12;let months=0,total=start;while(total<target&&months<600){total=(total+month)*(1+y);months++}return `<strong>${months>=600?'Цель требует пересмотра':Math.ceil(months/12)+' лет'}</strong>Ориентировочный срок достижения цели. Итоговая сумма: ${fmt(total)}.`}
function calcIis(){const amount=rangeVal('iis-amount'),tax=rangeVal('iis-tax')/100,years=rangeVal('iis-years'),yieldY=rangeVal('iis-yield')/100;const deduction=Math.min(amount,400000)*tax;let total=0;for(let i=0;i<years;i++){total=(total+amount+deduction)* (1+yieldY)}return `<strong>Вычет до ${fmt(deduction)} в год</strong>При ежегодном пополнении и реинвестировании вычета ориентировочный капитал через ${years} лет: ${fmt(total)}.`}
function calcBond(){const face=rangeVal('bond-face'),pricePct=rangeVal('bond-price'),couponPct=rangeVal('bond-coupon'),years=rangeVal('bond-years');const price=face*pricePct/100,coupon=face*couponPct/100,ytm=((coupon+(face-price)/years)/((face+price)/2))*100;return `<strong>YTM ≈ ${pct(ytm)}</strong>Цена покупки: ${fmt(price)}. Годовой купон: ${fmt(coupon)}. Купон и доходность могут отличаться.`}
function calcStock(){const price=rangeVal('stock-price'),shares=rangeVal('stock-shares'),div=rangeVal('stock-div'),growth=rangeVal('stock-growth')/100;const cost=price*shares,after=price*(1+growth)*shares,divs=div*shares*0.87;return `<strong>${fmt(after+divs-cost)}</strong>Потенциальный результат с учетом изменения цены и дивидендов после НДФЛ. Дивидендная доходность до налога: ${pct(div/price*100)}.`}
function calcFund(){const s=rangeVal('fund-stocks'),b=rangeVal('fund-bonds'),m=rangeVal('fund-money'),g=rangeVal('fund-gold'),total=s+b+m+g;return `<strong>${total===100?'Портфель собран на 100%':'Сумма долей: '+total+'%'}</strong>Акции отвечают за рост, облигации и денежный рынок — за устойчивость, золото — за дополнительную диверсификацию.`}
function calcGold(){const grams=rangeVal('gold-grams'),buy=rangeVal('gold-buy'),spread=rangeVal('gold-spread')/100,change=rangeVal('gold-change')/100;const cost=grams*buy,sell=grams*buy*(1+change)*(1-spread);return `<strong>${fmt(sell-cost)}</strong>Ориентировочный результат после изменения цены и спреда. Чем выше спред, тем больше должен вырасти металл, чтобы выйти в плюс.`}
